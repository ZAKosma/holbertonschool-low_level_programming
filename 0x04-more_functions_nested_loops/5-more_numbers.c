#include "main.h"

/**
 * print_numbers - returns the upperase character
 * Return: uppercase character
 */

void more_numbers()
{
	int i;
	int c;
	int offset = 48;

	for(i = 0; i < 10; i++)
	{
		for (c = 0; c < 15; c++)
		{
			if(c > 9)
			{
				_putchar(1 + offset);
			}

			_putchar((c % 10) + offset);
		}
		_putchar (10);
	}
}
